#entrada
numero_1 = int( input("Digite o primeiro numero: ") )
numero_2 = int( input("Digite o segundo numero: ") )
numero_3 = int( input("Digite o terceiro numero: ") )

#processamento
soma_dois_primeiros = numero_1 + numero_2
diferenca_dois_ultimos = numero_2 - numero_3

#saida
print("A soma dos números: ", soma_dois_primeiros)
print("A diferença dos números: ", diferenca_dois_ultimos)